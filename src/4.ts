// Define a function to calculate the area of a rectangle
function calculateArea(width, height) {
  return width * height;
}

// Use the function with different values for width and height
console.log("The area of a rect with w:10 h:5 is:", calculateArea(10, 5));
console.log("The area of a rect with w:4 h:2 is:", calculateArea(4, 2));
